package com.sessions.recap.JavaBasics;

public class DeclareAndInitializeVariables {

//    Declare and initialize variables: Declare and initialize variables of different data
//    types such as int, double, String, boolean, and char. Print out the values of these variables
//    to verify that they were initialized correctly.

    public static void main(String[] args) {
        int i=1;
        double d=2.5;
        String s="fsghr";
        boolean b=false;
        char c='c';

        System.out.println(i);
        System.out.println(d);
        System.out.println(s);
        System.out.println(b);
        System.out.println(c);
    }
}
