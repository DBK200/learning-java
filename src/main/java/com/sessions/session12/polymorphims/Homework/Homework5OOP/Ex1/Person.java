package com.sessions.session12.polymorphims.Homework.Homework5OOP.Ex1;

public class Person {

   private int age;
   private String name;

   public Person(int age, String name){
       this.name = name;
       this.age = age;
   }
   public int getAge(){
       return age;
   }
   public String getName(){
       return name;
   }
}
