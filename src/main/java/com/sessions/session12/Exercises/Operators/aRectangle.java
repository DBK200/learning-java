package com.sessions.session12.Exercises.Operators;

public class aRectangle {
//    Length and breadth of a rectangle are 5 and 7 respectively. Write a program to calculate the area and perimeter of the rectangle.
    public static void main(String[] args) {
        int lenght=5;
        int breadth=7;
        int area=lenght*breadth;
        int perimeter=2*(lenght+breadth);
        System.out.println("Area="+area);
        System.out.println("Perimeter="+perimeter);
    }
}
